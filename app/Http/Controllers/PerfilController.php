     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $perfil=new Perfil;
        $perfil->numero_documento=$request->numero_documento;
        $perfil->documento_id=$request->documento_id;
        $perfil->sexo=$request->sexo;
        $perfil->fecha_nacimiento=$request->fecha_nacimiento;
        $perfil->numero_celular=$request->numero_celular;
        $perfil->pregunta_id=$request->pregunta_id;
        $perfil->respuesta=$request->respuesta;
        $perfil->save();

        return response($perfil,200);
    }

